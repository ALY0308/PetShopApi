package domain;

public class QuotessDomain {
    private Integer idQuotes;
    private Integer petQuotes;
    private Integer ownerQuotes;
    private String dateQuotes;
    private String vetQuotes;


    public Integer getIdQuotes() {
        return idQuotes;
    }

    public void setIdQuotes(Integer idQuotes) {
        this.idQuotes = idQuotes;
    }

    public Integer getPetQuotes() {
        return petQuotes;
    }

    public void setPetQuotes(Integer petQuotes) {
        this.petQuotes = petQuotes;
    }

    public Integer getOwnerQuotes() {
        return ownerQuotes;
    }

    public void setOwnerQuotes(Integer ownerQuotes) {
        this.ownerQuotes = ownerQuotes;
    }

    public String getDateQuotes() {
        return dateQuotes;
    }

    public void setDateQuotes(String dateQuotes) {
        this.dateQuotes = dateQuotes;
    }

    public String getVetQuotes() {
        return vetQuotes;
    }

    public void setVetQuotes(String vetQuotes) {
        this.vetQuotes = vetQuotes;
    }
}
