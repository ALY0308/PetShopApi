package domain;

public class ProductsDomain {
    private Integer productProducts;
    private String categoryProducts;
    private Double priceProducts;
    private Integer amountProducts;


    public Integer getProductProducts() {
        return productProducts;
    }

    public void setProductProducts(Integer productProducts) {
        this.productProducts = productProducts;
    }

    public String getCategoryProducts() {
        return categoryProducts;
    }

    public void setCategoryProducts(String categoryProducts) {
        this.categoryProducts = categoryProducts;
    }

    public Double getPriceProducts() {
        return priceProducts;
    }

    public void setPriceProducts(Double priceProducts) {
        this.priceProducts = priceProducts;
    }

    public Integer getAmountProducts() {
        return amountProducts;
    }

    public void setAmountProducts(Integer amountProducts) {
        this.amountProducts = amountProducts;
    }
}
